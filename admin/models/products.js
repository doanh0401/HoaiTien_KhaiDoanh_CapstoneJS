function Product (id, name, price, image, screen, backCamera, frontCamera, describe, type) {
    this.id = id;
    this.name = name;
    this.price = price;
    this.img = image;
    this.screen = screen;
    this.backCamera = backCamera;
    this.frontCamera = frontCamera;
    this.desc = describe;
    this.type = type;
};